package cs3500.pa05.model;

/**
 * The possible theme options
 */
public enum Theme {
  /**
   * Option for the theme format provided in the green.css file
   */
  GREEN,
  /**
   * Option for the theme format provided in the pink.css file
   */
  PINK,
  /**
   * Colors
   */
  BLUE,
  /**
   * Option for resetting theme format provided by JavaFX
   */
  RESET
}
